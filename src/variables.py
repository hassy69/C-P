# Déclaration et initialisation des variables

# Variable entière (int)
int_value = 55

# Variable flottante (float)
float_value = 89.14159

# Variable chaîne de caractères (str)
str_value = "Bonjour le monde!"

# Variable booléenne (bool)
bool_value = True

# Variable liste (list)
list_value = [1, 2, 3]

# Variable tuple (tuple)
tuple_value = (1, 2, 3)

# Variable dictionnaire (dict)
dict_value = {'jul': 'ovni', 'morts': 25,}

# Affichage des valeurs
print(f"Valeur de int_value : {int_value}")
print(f"Valeur de float_value : {float_value}")
print(f"Valeur de str_value : {str_value}")
print(f"Valeur de bool_value : {bool_value}")
print(f"Valeur de list_value : {list_value}")
print(f"Valeur de tuple_value : {tuple_value}")
print(f"Valeur de dict_value : {dict_value}")
